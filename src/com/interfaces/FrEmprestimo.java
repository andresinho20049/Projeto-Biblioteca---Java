    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        jTable1.setDefaultEditor(Object.class, null);
        if(evt.getClickCount() == 2){
            id_acervo.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
            nm_acervo.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString());
            valor_unit = (double) jTable1.getValueAt(jTable1.getSelectedRow(), 2);
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(id_acervo.getText().isEmpty() || nm_acervo.getText().isEmpty() || qtde.getText().isEmpty()|| dt_previsto.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Favor, preencha todos os campos!!!");
        }else{
        
        valor_total = valor_unit * Integer.parseInt(qtde.getText());
        
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        
        model.addRow(new Object[]{
            Integer.parseInt(id_acervo.getText()),
            nm_acervo.getText(),
            valor_unit,
            Integer.parseInt(qtde.getText()),
            valor_total,
            dt_previsto.getText()            
        });
        
        id_acervo.setText("");
        nm_acervo.setText(""); 
        qtde.setText(""); 
        dt_previsto.setText("");
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void EnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EnviarActionPerformed
        // TODO add your handling code here:
        int linha;
    for (linha=0;linha<jTable2.getRowCount();linha++){
        Locacao p = new Locacao();
        LocacaoDao l = new LocacaoDao();
        Acervo a = new Acervo();
        Cliente c = new Cliente();
        NotaFiscal nf = new NotaFiscal();
           a.setId((int) jTable2.getValueAt(linha, 0));
           a.setNome(jTable2.getValueAt(linha, 1).toString());
           p.setAcervo(a);
           p.setQuantidade((int) jTable2.getValueAt(linha, 3));
           p.setValor((double) jTable2.getValueAt(linha, 4));
           p.setData(jTable2.getValueAt(linha, 5).toString());
           nf.setId(Integer.parseInt(nnf.getText()));
           p.setNf(nf);
           c.setId(Integer.parseInt(id_cliente.getText()));
           p.setCliente(c);
           l.create(p);
    }        
    
    JOptionPane.showMessageDialog(null, "Emprestimo de " + linha + " itens realizado com sucesso!!!");
    
    }//GEN-LAST:event_EnviarActionPerformed
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (jTable2.getRowCount() == 0){
            JOptionPane.showMessageDialog(null, "Adicione itens ao carrinho!");
        }else{
        
        NotaFiscal nf = new NotaFiscal();
        NotaFiscalDao nfD = new NotaFiscalDao();
        Usuario usu = new Usuario();
        
        usu.setId(id_usuario);
        nf.setUsuario(usu);
        nfD.create(nf);
        nf.getId();
        nnf.setText(String.valueOf(nf.getId()));
        
        Enviar.setEnabled(true);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    
    
