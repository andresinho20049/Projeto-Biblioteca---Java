    public void RetornaDados(Cliente c){
        
        switch (c.getTipo()) {
            case 0:                
                id_cliente.setText(String.valueOf(c.getId()));
                nm_cliente.setText(c.getNome());
                jbl1.setText("Nome");
            break;
            case 1:             
                id_cliente.setText(String.valueOf(c.getId()));
                nm_cliente.setText(c.getNome());
                jbl1.setText("Razão Social");
                
            break;
            default:
                JOptionPane.showMessageDialog(null, "Opção Invalida!!!");
            }
    } 
    
    public void read_jtable(){
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setNumRows(0);
        
        AcervoDao pdao = new AcervoDao();
        for (Acervo p : pdao.findAll()) {
            model.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                p.getValor(),
                p.getIsqn(),
                p.getAutor().getNome(),
                p.getEditora().getNome(),
                p.getGenero().getNome()                
            });
    }
    }
    
